import 'package:json_parser/json_parser.dart';

class PostModel extends IBaseModel<PostModel> {
  PostModel({
    this.title,
    this.body,
    this.userId,
  });

  String? title;
  String? body;
  int? userId;

  @override
  fromJson(Map<String, dynamic> json) => PostModel(
        title: json["title"],
        body: json["body"],
        userId: json["userId"],
      );

  @override
  toJson() => {
        "title": title,
        "body": body,
        "userId": userId,
      };
}

